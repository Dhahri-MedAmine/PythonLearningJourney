from numbers import Number


def mult(a: Number, b: Number) -> Number:
    """Return the Product of 'a' and 'b'

    Args:
        a (Number): 1st Number
        b (Number): 2nd Number

    Returns:
        Number: product of 'a' and 'b'
    """
    return a * b  # type: ignore
